a = [1,2,3,4]

#列表的长度
print(len(a))

#列表的乘法操作，将列表里面的元素重复指定的次数
print(a*4)
print('*'*30)    #可以用来制作分割线

#判断指定的元素是否存在在列表中
print(1 in a)   #true
print(99 in a)  #false

#迭代列表中的每一个元素,利用循环
for x in a:
    print(x)



