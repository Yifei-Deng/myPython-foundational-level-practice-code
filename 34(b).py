if '':
    print("'' is True.")
else:
    print("'' is False.")

if ' ':
    print("' ' is True.")
else:
    print("' ' is False.")


if 0:
    print("0 is True.")
else:
    print("0 is False.")

if ():
    print("() is True.")
else:
    print("() is False.")

if []:
    print("[] is True.")
else:
    print("[] is False.")

if {}:
    print("{} is True.")
else:
    print("{} is False.")

if None:
    print("None is True.")
else:
    print("None is False.")

if False:
    print("False is True.")
else:
    print("False is False.")




